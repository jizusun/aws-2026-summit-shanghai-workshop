def build_knowledge_base(output_dir: str = "knowledge_base"):
    """从写死的政策模板生成 markdown 文档（无外部数据依赖）。"""
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)
    for domain, policy in DOMAIN_POLICIES.items():
        lines = [f"# {policy['title']}\n"]
        for section_title, section_content in policy["sections"]:
            lines.append(f"\n## {section_title}\n")
            lines.append(section_content)
        # 追加 HR-MultiWOZ 派生的 FAQ（有意保留的噪声，见模块 docstring）
        faqs = DOMAIN_FAQS.get(domain, [])
        if faqs:
            lines.append("\n## 常见问题\n")
            for q, a in faqs[:10]:
                lines.append(f"**问：{q}**\n答：{a}\n")
        doc_path = out / f"{domain}.md"
        doc_path.write_text("\n".join(lines), encoding="utf-8")
        print(f"  {domain}: {len(policy['sections'])} 章节 + {len(faqs)} FAQ -> {doc_path}")
    print(f"\n知识库文档生成完成：{len(DOMAIN_POLICIES)} 个文档，保存在 {out}/")


if __name__ == "__main__":
    output = sys.argv[1] if len(sys.argv) > 1 else "knowledge_base"
    build_knowledge_base(output)
